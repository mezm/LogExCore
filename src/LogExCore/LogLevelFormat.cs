namespace LogExCore
{
    /// <summary>
    /// Log level format
    /// </summary>
    public enum LogLevelFormat
    {
        /// <summary>
        /// One level code, e.g. D
        /// </summary>
        L1,
        /// <summary>
        /// Three level code, e.g. ERR
        /// </summary>
        L3,
        /// <summary>
        /// Full log level name, e.g. DEBUG
        /// </summary>
        Full
    }
}
